function introduce(name, age) {
    if (age !== undefined) {
        return "\u041C\u0435\u043D\u044F \u0437\u043E\u0432\u0443\u0442 ".concat(name, ", \u043C\u043D\u0435 ").concat(age, " \u043B\u0435\u0442");
    }
    return "\u041C\u0435\u043D\u044F \u0437\u043E\u0432\u0443\u0442 ".concat(name);
}
